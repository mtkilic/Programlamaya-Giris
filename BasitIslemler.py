birinci_sayi = int(input("Lutfen birinci sayiyi giriniz: "))
ikinci_sayi = int(input("Lutfen ikinci sayiyi giriniz: "))

print("Toplam: {}".format(birinci_sayi + ikinci_sayi))
print("fark: {}".format(birinci_sayi - ikinci_sayi))
print("carpim: {}".format(birinci_sayi * ikinci_sayi))
print("bolum: {}".format(birinci_sayi / ikinci_sayi))
print("kalan: {}".format(birinci_sayi % ikinci_sayi))
